#1.List operations
marks=[30,50,40,60]
print("Marks= ",marks)
print("Sum= ",sum(marks))  #sum of items of list
product=1
for i in marks:
    product=product*i
print("Product= ",product)   #product of items of list
print("Highest mark= ",max(marks))   #largest item from list
print("Lowest mark= ",min(marks))   #smallest item from list

